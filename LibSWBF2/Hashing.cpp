	CRCChecksum CRC::CalcLowerCRC(const char* str)
	{
		if (str == nullptr)
		{
			LOG_ERROR("Given string pointer was NULL!");
			return 0;
		}

		CRCChecksum crc = 0xFFFFFFFF;
		for (const unsigned char* p = (const unsigned char*)str; *p; p++)
		{
			crc = (crc << 8) ^ m_Table32[(crc >> 24) ^ m_ToLower[*p]];
		}
		return ~crc;
	}

	FNVHash FNV::Hash(const Types::String& str)
	{
		constexpr uint32_t FNV_prime = 16777619;
		constexpr uint32_t offset_basis = 2166136261;

		uint32_t hash = offset_basis;

		const char* buffer = str.Buffer();
		for (size_t i = 0; i < str.Length(); ++i)
		{
			char c = buffer[i];
			c |= 0x20;

			hash ^= c;
			hash *= FNV_prime;
		}

		return hash;
	}

	bool FNV::Lookup(FNVHash hash, Types::String& result)
	{
		const auto it = m_LookupTable.find(hash);
		if (it != m_LookupTable.end())
		{
			result = it->second.data();
			return true;
		}
		return false;
	}

	constexpr FNVHash FNV::HashConstexpr(const std::string_view str)
	{
		constexpr uint32_t FNV_prime = 16777619;
		constexpr uint32_t offset_basis = 2166136261;

		uint32_t hash = offset_basis;

		for (auto c : str)
		{
			c |= 0x20;

			hash ^= c;
			hash *= FNV_prime;
		}

		return hash;
	}

	constexpr FNVHash operator""_fnv(const char* str, const std::size_t length)
	{
		return FNV::HashConstexpr({ str, length });
	}

	constexpr std::pair<FNVHash, std::string_view> operator""_fnvp(const char* str,
		std::size_t length)
	{
		return { FNV::HashConstexpr({str, length}), {str, length} };
	}

